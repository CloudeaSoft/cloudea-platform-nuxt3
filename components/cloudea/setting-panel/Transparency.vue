<script setup lang="ts">
const colorMode = useColorMode()
const { systemPageTransparency } = storeToRefs(useSettingStore())

watch(
  () => systemPageTransparency.value,
  () => {
    useSettingStore().setPageTransparency(
      systemPageTransparency.value,
      colorMode.value as 'dark' | 'light'
    )
  }
)

watch(
  () => colorMode.value,
  () => {
    useSettingStore().setPageTransparency(
      systemPageTransparency.value,
      colorMode.value as 'dark' | 'light'
    )
  }
)
</script>

<template>
  <div>
    <div class="container">
      <span>{{ $t('settings.trans') }}</span>
      <span>{{ systemPageTransparency }}%</span>
    </div>

    <div class="scroll">
      <span>10%</span>
      <input
        class="main"
        min="10"
        max="90"
        step="1"
        type="range"
        v-model="systemPageTransparency"
        @touchmove.stop.passive
      />
      <span>90%</span>
    </div>
  </div>
</template>

